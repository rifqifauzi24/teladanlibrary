            <h2>Sejarah Perpustakaan Teladan 82</h2>
            <p>Pada tahun 1950 sekolah VHC merupakan 2 sekolah. Yang pagi dibawah pimpinan Hr. Bruyn berdomisili di Jl. Diponegoro yang dan diberi nama SMA Istimewa. pada tahun 1951, kepemimpinan SMA Istimewa diserahkan kepada Bp. Ibnu Tadjie dan diberi nama SMA Negeri V ABC. karena Bp. Ibnu Tadjie merangkap sebagai anggota DPRS, maka pimpinan sekolah diserahkan kepada Bapak Benyamin Prawirohatmojo, yang lebih dikenal dengan panggilan Pak Ben. Beberapa waktu kemudian SMA V ABC dipecah lagi menjadi 2, yang salah satunya adalah SMA II B yang berdomisili di Jl. Batu dan dipimpin oleh Bapak Arjo.</p>
            
            <p>Pada tahun 1954 SMA II B pindah kegedung baru jl. Setiabudi II, yang sebetulnya selesai dibangun pada tahun 1953. Atas prakarsa Inspeksi pusat dan KorpsnGuru SMA II B , sekolah ini kemudian dijadikan model, yang kemudian menjadi contoh bagi SMA-SMA di seluruh Indonesia. karenanya dalam segala hal, sekolah ini mendapat fasilitas dari insfeksi pusat, sehingga dapat mengadakan percobaan dan pratikum sendiri. Sekolah ini akhirnya diberi nama baru yaitu SMA Teladan Bagian B. Teladan bagian C terdapat disurabaya. Pada perkembangannya selanjutnya sekolah ini dikenal dengan nama SMAN 3 Teladan.</p>
                
            <p>Selanjutnya secara berturut-turut sekolah ini dipimpin oleh Bp. Soegijanto, Bp. Tan Kiong Oey, Bp. Jasin Arifin, Bp. J. Alfons, Bp. Joko Soedibjo, Bp. Tommy N.S, Ibu Pratiwi, Bp. Djoko Waliadi, Bp. Rosman Harahap, Bp. N.T. Padidi, Bp. Joelius Joesoep, Bp. Soetopo, Bp. Asrul Chatib, Bp. Tatang Sutarsa, Bp. Sidik Yasin, Bp. Suyitno sampai dengan Februari 2007.Bp. Drs. Suyitno, M.pd. Selanjutnya digantikan oleh Ibu Hj. Wieke Salehani M.Pd.</p>

            <p>SMA Negeri 3 Jakarta juga memiliki berbagai macam kegiatan dan acara tahunan yang cukup dikenal di kalangan pelajar. Salah satunya adalah Anttic (Anak Teladan Tiga Cup). Kompetisi antar sekolah ini pertama kali dilaksanakan pada tahun 2006, dengan mengundang beberapa sekolah untuk berpartisipasi. Pada tahun 2009, SMAN 3 berhasil memperbesar kegiatan Anttic dengan memindahkan lokasi acara dan mengundang beberapa artis lokal. Pada tahun yang sama pula, Anttic dinobatkan menjadi kegiatan pelajar SMA paling bergengsi di Jakarta. Pada tahun 2010, Anttic menjadi kegiatan pelajar SMA terbesar di Jakarta.</p>
                
            <p>Selain itu, SMA Negeri 3 Jakarta juga mengadakan acara tahunan TRAP (Teladan seminaR And workshoP). Acara yang diadakan oleh ekskul Science Club SMAN 3 ini diperuntukkan untuk murid SMA se-Jabotabek. Acara ini berupa workshop dan seminar tentang sains. Acara ini juga merupakan hasil kerja sama dengan forum KIR se-Jabotabek, FOSCA</p>
                
            <p>Hal menarik dari SMA Negeri 3 Jakarta adalah gudang makanan enak dan murah yang direpresentasikan melalui kantin kejujuran dan kantin sehat.</p>

